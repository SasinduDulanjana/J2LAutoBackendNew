package com.example.smartPos.controllers.responses;

import com.example.smartPos.repositories.model.Customer;
import com.example.smartPos.repositories.model.Supplier;
import com.example.smartPos.util.PaymentType;
import com.example.smartPos.util.ReferenceType;

import java.util.Date;

public class PaymentResponse {
    private Integer paymentId;
    private PaymentType paymentType; // RECEIPT or PAYMENT
    private String referenceId; // Links to sale_id or purchase_id
    private ReferenceType referenceType; // SALE or PURCHASE
    private Customer customer;
    private Supplier supplier;
    private Date paymentDate;
    private Double totalAmount;
    private String remarks;

    public Integer getPaymentId() {
        return paymentId;
    }

    public void setPaymentId(Integer paymentId) {
        this.paymentId = paymentId;
    }

    public PaymentType getPaymentType() {
        return paymentType;
    }

    public void setPaymentType(PaymentType paymentType) {
        this.paymentType = paymentType;
    }

    public String getReferenceId() {
        return referenceId;
    }

    public void setReferenceId(String referenceId) {
        this.referenceId = referenceId;
    }

    public ReferenceType getReferenceType() {
        return referenceType;
    }

    public void setReferenceType(ReferenceType referenceType) {
        this.referenceType = referenceType;
    }

    public Customer getCustomer() {
        return customer;
    }

    public void setCustomer(Customer customer) {
        this.customer = customer;
    }

    public Supplier getSupplier() {
        return supplier;
    }

    public void setSupplier(Supplier supplier) {
        this.supplier = supplier;
    }

    public Date getPaymentDate() {
        return paymentDate;
    }

    public void setPaymentDate(Date paymentDate) {
        this.paymentDate = paymentDate;
    }

    public Double getTotalAmount() {
        return totalAmount;
    }

    public void setTotalAmount(Double totalAmount) {
        this.totalAmount = totalAmount;
    }

    public String getRemarks() {
        return remarks;
    }

    public void setRemarks(String remarks) {
        this.remarks = remarks;
    }
}